package castingstudy;

public class premitive_casting {

	public static void main(String[] args) {
		// converting one type of information into another type is called casting
		// converting one data type into another data type is called primitive casting
		int a= 10;
		System.out.println(a);
		double b= a;
		System.out.println(b);// implicit casting lower data type in higher type
		System.out.println("==============");
		
		double c =2.5;
		System.out.println(c);
		int d= (int) c;
		System.out.println(d);// explicit casting higher data type into lower data type
		
		
		
		
	}

}
